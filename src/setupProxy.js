const proxy = require('http-proxy-middleware');

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 클라이언트(React)와 서버(Express)를 연결시켜주는 프록시 설정
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
module.exports = (app) => {
  app.use(
    proxy.createProxyMiddleware('/api', {
      target: 'http://localhost:3001'
    })
  )
};